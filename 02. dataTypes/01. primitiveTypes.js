// PRIMITIVE TYPES

// 1. String
const name = 'John Doe'
console.log(typeof name) // string

// 2. Number
const age = 2
console.log(typeof age) // number

// 3. Boolean
const isTrue = false
console.log(typeof isTrue) // boolean

// 4. null
const fact = null
console.log(typeof fact) // object, this is a bug in JavaScript

// 5. Undefined
let car
console.log(typeof car) // undefined

// 6. Symbol
const sym = Symbol()
console.log(typeof sym) // symbol
